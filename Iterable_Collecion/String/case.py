str = "Hi, This is Harini working in Expeditors"
#uppercase - Converting the string to upper case
print("Uppercase:",str.upper())
#lowercase - Converting the string to lowercase
print("Lowercase:",str.lower())
#swapcase- Converting the upper into lower and lower into upper case
print("Swapcase:",str.swapcase())
#Title - Capiatlizing each word in the string
print("Title:",str.title())
#capitalize - Just capitalising the first character
print("Capitalize:",str.capitalize())